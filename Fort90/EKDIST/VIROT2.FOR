	SUBROUTINE VIROT2(KBUF,NDIM,N)
c
c Integer*2 version of VIROT
C SUBROUTINE, SIMPLER THAN VECROT, TO MOVE INTEGER ARRAY
C N PLACES TO RIGHT (N POS) OR LEFT (N NEG) WITHOUT WRAP-ROUND
C RESETS UNUSED ELEMENTS TO ZERO
	integer*2 kbuf(ndim)
C
	IF(N.EQ.0) RETURN
	IF(N.LT.0) GOTO 2
	DO 1 I=NDIM,N+1,-1
1	KBUF(I)=KBUF(I-N)
	DO 5 I=1,N
5	KBUF(I)=0
	RETURN
C NOW LEFT SHIFT
2	CONTINUE
	N=-N	!SO POS
	DO 3 I=1,NDIM-N
3	KBUF(I)=KBUF(I+N)
	DO 4 I=NDIM-N+1,NDIM
4	KBUF(I)=0
	RETURN
	END


