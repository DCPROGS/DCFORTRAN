	SUBROUTINE CUMGAM(NCOMP,ISG,YLOW,YHIGH,AMEAN,AREA,F1,F2)
	REAL AMEAN(10),AREA(10)
C TO CALC CUMULATIVE GAMMA DIST (NCOMP=1) OR EQUIV FOR 2- EXP
C INTERVALS (NCOMP=2) SEE MLFIT1. CALLED BY PTYP1 AND GETPAR.
	IF(NCOMP.EQ.2) GOTO 13
	DO 39 I=0,ISG
	T=1.0
	DO 524 J=1,I
524	T=T*FLOAT(J)		!T=FACTORIAL(I)
	F1=F1+((YLOW/AMEAN(1))**I)*EXP(-YLOW/AMEAN(1))/T
	F2=F2+((YHIGH/AMEAN(1))**I)*EXP(-YHIGH/AMEAN(1))/T
39	CONTINUE
	RETURN
C
13	CONTINUE
	DO 14 J=1,2
	F1=F1+AREA(J)*AREA(J)*(1.0+YLOW/AMEAN(J))*EXP(-YLOW/AMEAN(J))
14	F2=F2+AREA(J)*AREA(J)*(1.0+YHIGH/AMEAN(J))*EXP(-YHIGH/AMEAN(J))
	X=2.0*AREA(1)*AREA(2)/(1.0/AMEAN(2)-1.0/AMEAN(1))
	F1=F1+X*(EXP(-YLOW/AMEAN(1))/AMEAN(2)-
     & EXP(-YLOW/AMEAN(2))/AMEAN(1))
	F2=F2+X*(EXP(-YHIGH/AMEAN(1))/AMEAN(2)-
     & EXP(-YHIGH/AMEAN(2))/AMEAN(1))
	RETURN
	END


