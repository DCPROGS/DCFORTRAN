	subroutine VAR(Y,N,ybar,vary)
C	CALCULATES MEAN,YBAR,AND VARIANCE,VARY OF FIRST N
C	ELEMENTS OF Y. SINGLE PASS YOUNGS AND CRAMER METHOD.
	dimension Y(N)
c
	SUMY=Y(1)
	SUMYY=0.
c
	DO 1 I=2,N
	YI=Y(I)
	FI=FLOAT(I)
	SUMY=SUMY+YI
	SUMYY=SUMYY+(FI*YI-SUMY)**2/(FI*(FI-1))
1	CONTINUE
	YBAR=SUMY/FLOAT(N)
	VARY=SUMYY/FLOAT(N-1)
	RETURN
	END
